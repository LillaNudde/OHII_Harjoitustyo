package com.github.LillaNudde;

public class DataObject
{
    private int ID;
    private String date;
    private double length;
    private double width;
    private double thickness;

    public DataObject(int ID, String date, double length, double width, double thickness)
    {
        this.ID = ID;
        this.date = date;
        this.length = length;
        this.width = width;
        this.thickness = thickness;
    }

}
